package com.iteam.voiceplusmobile.ui.pricing;

public class CustomListDataModel {

    private int image_id;
    private String phone_model_name;
    private String repair_part_name;
    private String mobile_company;
    private int repairing_price;
    private String repairing_description;

    public int getImage_id() {
        return image_id;
    }

    public void setImage_id(int image_id) {
        this.image_id = image_id;
    }

    public String getPhone_model_name() {
        return phone_model_name;
    }

    public void setPhone_model_name(String phone_model_name) {
        this.phone_model_name = phone_model_name;
    }

    public String getRepair_part_name() {
        return repair_part_name;
    }

    public void setRepair_part_name(String repair_part_name) {
        this.repair_part_name = repair_part_name;
    }

    public String getMobile_company() {
        return mobile_company;
    }

    public void setMobile_company(String mobile_company) {
        this.mobile_company = mobile_company;
    }

    public int getRepairing_price() {
        return repairing_price;
    }

    public void setRepairing_price(int repairing_price) {
        this.repairing_price = repairing_price;
    }

    public String getRepairing_description() {
        return repairing_description;
    }

    public void setRepairing_description(String repairing_description) {
        this.repairing_description = repairing_description;
    }






}
